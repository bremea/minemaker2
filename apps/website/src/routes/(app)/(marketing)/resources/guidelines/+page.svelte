	<h1 class="text-3xl font-bold">Minemaker Community Guidelines</h1>
	<p>Last Updated: July 13, 2025</p>
	<br />
	<p>
		Welcome to Minemaker! <br />
		<br />
		Minemaker is a platform where people from all around the world come together to create, share, and
		play. We welcome players and creators of all ages, races, ethnicities, religions, abilities, sexual
		orientations, and gender identities. <br />
		<br />
		To help keep Minemaker a safe, inclusive, and fun environment for everyone, we ask all players to
		follow these Community Guidelines: <br />
		<br />
		<span class="text-2xl font-bold">Follow Minecraft's EULA</span> <br />
		Just as Minemaker as a platform has to follow Minecraft's EULA and Usage Guidelines, so do all of
		our players. These community guidelines should be considered an extension to Mojang's guidelines.
		The Minemaker team will enforce Minecraft's EULA just as we enforce our own policies.
		<br /> <br />
		<span class="text-2xl font-bold">Respect Others</span> <br />
		Treat everyone with kindness, respect, and empathy at all times. Do not engage in harassment, bullying,
		hate speech, or discrimination of any kind. Treat others how you want to be treated, and keep the
		community a welcoming place for all.
		<br /> <br />
		<span class="text-2xl font-bold">Keep it Clean</span> <br />
		Content that promotes violence, self-harm, illegal activity, or explicit material is not allowed
		on Minemaker.
		<br /> <br />
		<span class="text-2xl font-bold">Build Fair, Play Fair</span> <br />
		Cheating, exploiting, using hacks/unapproved mods/tools to gain any kind of unfair advantage is not
		allowed. Additionally, any attempts to compromise the security of the platform — including uploading
		malicious plugins, exploiting system vulnerabilities, or trying to gain unauthorized access to our
		platform — are strictly prohibited.
		<br /> <br /><span class="text-2xl font-bold">Share Responsibly</span> <br />
		Only share content you have the rights to use. Don't upload stolen assets or copyrighted material
		that you don't have permission to use. Don't attempt to steal or misappropriate the work of others.
		<br /> <br />
		<span class="text-2xl font-bold">Protect Privacy</span> <br />
		Don't share any private or identifying information about yourself or others. This includes real names,
		addresses, contact info, phone numbers, or email addresses.
		<br /> <br />
		<span class="text-2xl font-bold">Support Others</span> <br />
		Minemaker thrives as a community when players and creators help each other. Offer constructive feedback
		when commenting on the creations of others. Toxicity, trolling, and brigading is not allowed.
		<br /> <br />
		<span class="text-2xl font-bold">See Something, Say Something</span> <br />
		Minemaker is actively moderated. If you come across content in violation of these guidelines, please
		use the “Report” button to let us know. Use this tool responsibly - don't create false or spam reports.
		<br />
		<br />
		Minemaker is built and protected by our community. These guidelines are in place so that everyone
		can create and play without fear or negativity. Please help us keep Minemaker safe, fair, and fun
		for all by adhering to these guidelines - whether that be in game, online, or even in community spaces
		on third-party platforms. It is up to every member of our community to foster a safe place for everyone.
	</p>
</main>
